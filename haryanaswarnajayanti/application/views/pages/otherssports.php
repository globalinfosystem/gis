<p>Dhyan Chand Award is India's highest award for lifetime achievement in sports and games, given by the <strong>Government of India</strong>. The award is named after the legendary <strong>Indian hockey player Dhyan Chand</strong>. The award was initiated in 2002. The award carries a cash prize of <strong>INR 500,000,</strong> a plaque and a scroll of honour.</p>


